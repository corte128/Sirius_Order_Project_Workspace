package com.sirius.order.service.attendance.beans;

public class AttendanceRecordBean {

	String attendantName = null;
	String attendantEmail = null;
	String attendantDate = null;
	String attendantLocation = null;
	
	
	public String getAttendantName() {
		return attendantName;
	}
	public void setAttendantName(String attendantName) {
		this.attendantName = attendantName;
	}
	public String getAttendantEmail() {
		return attendantEmail;
	}
	public void setAttendantEmail(String attendantEmail) {
		this.attendantEmail = attendantEmail;
	}
	public String getAttendantDate() {
		return attendantDate;
	}
	public void setAttendantDate(String attendantDate) {
		this.attendantDate = attendantDate;
	}
	public String getAttendantLocation() {
		return attendantLocation;
	}
	public void setAttendantLocation(String attendantLocation) {
		this.attendantLocation = attendantLocation;
	}
	
	
}
